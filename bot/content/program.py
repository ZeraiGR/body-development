def week_for_day(day: int) -> int:
    """День 1..30 -> неделя 1..4 (по MONTH_1_PLAN)."""
    if day <= 7:
        return 1
    if day <= 14:
        return 2
    if day <= 21:
        return 3
    return 4


def stage_for_day(day: int) -> tuple[str, str]:
    """Вернуть (название этапа, научный факт) для дня программы."""
    for lo, hi, name, fact in STAGES:
        if lo <= day <= hi:
            return name, fact
    # За пределами 30 дней — последний этап.
    return STAGES[-1][2], STAGES[-1][3]


def week_actions(week: int) -> dict:
    """Данные недели из MONTH_1_PLAN."""
    return WEEKS[week]
