// Конвейер переписывания статей 2-30.
// Стадии: кинезиолог (факты) || IT-специалист (аналогии) → редактор.
// IT-аналогии НЕ запрещены: оставляем адекватные и технически-корректные
// (по вердикту IT-специалиста), удаляем только натянутые/неверные.
// Эталон стиля — Day 1 (data/articles/day_1.json, перезалит 07-14).
// Перезалив на Telegraph: rsync data/articles/ → VPS, затем
//   for d in $(seq 2 30); do ssh root@194.62.66.34 "cd /root/body-development && .venv/bin/python -m scripts.reupload_article $d"; done
export const meta = {
  name: 'content-overhaul-days-2-30',
  description: 'Переписать статьи 2-30: кинезиолог + IT-специалист → редактор (чистый формат, Эббингауз, адекватные IT-аналогии)',
  phases: [
    { title: 'Validate', detail: 'кинезиолог (факты) и IT-специалист (аналогии) — параллельно' },
    { title: 'Rewrite', detail: 'редактор переписывает по обоим брифам' },
  ],
}

const DAYS = Array.from({ length: 29 }, (_, i) => i + 2) // 2..30

const STYLE = [
  'Аудитория: разработчик, много сидит; гиперлордоз, кифоз, асимметрия (сдвиг влево), "каменная" правая сторона, слабая левая. Не любит напрягаться, бросает рутины.',
  'Тон: тёплый, как друг-разработчик объясняет. БЕЗ умничанья, БЕЗ давления, БЕЗ натянутых аналогий.',
  'Длина: ~13000-16000 символов (чуть больше, если тема дня позволяет углубление). Одна-две главные мысли, глубоко и понятно.',
  'Формат: чистый Markdown, хорошо рендерящийся в Telegraph (##/###, таблицы | |, > цитаты, **жирный**, списки). НИКАКОЙ псевдографики/ASCII-артов. Эмодзи умеренно, по делу.',
  'IT-АНАЛОГИИ НЕ ЗАПРЕЩЕНЫ. Аналогия с кодом/сервером/памятью уместна, если она (а) технически корректна и (б) реально помогает понять медицинский концепт. Оставляй те, что одобрил IT-специалист (it_keep), добавляй предложенные им (it_suggest) где уместно, удаляй только натянутые/неверные (it_cut). Не вставляй аналогии ради красного словца.',
  'Где уместно визуально — вставь [РИСУНОК: точное описание, что именно показать] отдельной строкой (позже заменим на реальные картинки).',
].join(' ')

const ANCHOR = 'Опорные термины ПРЕДЫДУЩИХ дней, к которым надо возвращаться (кривая забывания Эббингауза) — упоминай их с коротким напоминанием там, где речь о связанном: "губка диска", "питание диска через смену нагрузки (осмос)", "длительность важнее позы", "давление в диске", "микропауза/правило 20-8", "фасции", "гипертонус", "триггерные точки", "диафрагмальное дыхание", "лордоз/кифоз", "асимметрия/сдвиг влево". Свои новые термины вводи с коротким определением при первом упоминании.'

const KINESIO_SCHEMA = {
  type: 'object',
  properties: {
    facts_corrections: { type: 'string', description: 'Неточные факты и исправления (с источником/консенсусом). Если всё верно — так и сказать.' },
    metaphors_to_keep: { type: 'string', description: 'Точные простые НЕ-IT образы (губка, полотенце, пружина), которые ОСТАВИТЬ.' },
    new_terms: { type: 'string', description: 'Ключевые новые термины этого дня (с коротким определением).' },
    must_convey: { type: 'string', description: '3-5 фактов, которые статья обязана донести.' },
  },
  required: ['facts_corrections', 'metaphors_to_keep', 'must_convey'],
}

const IT_SCHEMA = {
  type: 'object',
  properties: {
    it_keep: { type: 'string', description: 'IT-аналогии, которые ОСТАВИТЬ (технически корректны + реально помогают понять). Для каждой — почему она хороша.' },
    it_cut: { type: 'string', description: 'IT-аналогии, которые УДАЛИТЬ (натянуты / технически некорректны / дублируют). Для каждой — почему.' },
    it_suggest: { type: 'string', description: 'Если где-то хорошая точная IT-аналогия уместна и её нет — предложи 1-2 (с какой медицинской идеей связать).' },
  },
  required: ['it_keep', 'it_cut'],
}

const results = await pipeline(
  DAYS,
  (day) => parallel([
    () => agent(
      `Ты — кинезиолог/физиотерапевт (evidence-based). Прочитай файл data/articles/day_${day}.json (поле body_markdown) — статья дня ${day} 30-дневного курса реабилитации спины для разработчика. Провалидируй БИОМЕХАНИКУ (не IT-аналогии — их оценивает другой агент). Выдай бриф: факты_исправления (что неточно — исправь с источником), образы_оставить (точные простые НЕ-IT образы), новые_термины (ключевые термины дня с определением), обязательное (3-5 фактов). Кратко и конкретно. На русском.`,
      { label: `kines:d${day}`, phase: 'Validate', schema: KINESIO_SCHEMA }
    ),
    () => agent(
      `Ты — опытный IT-специалист (senior developer). Читаешь научно-популярную статью о здоровье спины ДЛЯ разработчиков: data/articles/day_${day}.json (поле body_markdown). В статье есть IT-аналогии (сравнения биомеханики с кодом/сервером/памятью/CPU/HTTP/бд и т.п.). Оцени КАЖДУЮ IT-аналогию по двум критериям: (1) технически корректна ли (нет ли фактических ошибок про IT), (2) адекватна ли — реально помогает понять медицинский концепт или натянута и сбивает. Выдай: it_keep (какие ОСТАВИТЬ — корректны и помогают, с почему), it_cut (какие УДАЛИТЬ — натянуты/некорректны/дублируют, с почему), it_suggest (если для ключевого концепта уместна точная IT-аналогия, которой нет — предложи 1-2). Помни: IT-аналогии разрешены и желательны для этой аудитории, но только адекватные и проверенные. Кратко. На русском.`,
      { label: `it:d${day}`, phase: 'Validate', schema: IT_SCHEMA }
    ),
  ]),
  (briefs, day) => agent(
    `Ты — редактор научно-популярного текста о здоровье. Перепиши статью дня ${day}.

ПРОЧЕСТЬ: data/articles/day_${day}.json (поле body_markdown) — текущая версия.

СТИЛЬ (утверждён на Day 1): ${STYLE}

ЗАКРЕПЛЕНИЕ (Эббингауз): ${ANCHOR}

БРИФ КИНЕЗИОЛОГА (примени ВСЕ исправления фактов, сохрани точные образы): ${JSON.stringify(briefs[0])}

БРИФ IT-СПЕЦИАЛИСТА (IT-аналогии: оставь it_keep, добавь it_suggest где уместно, удали it_cut): ${JSON.stringify(briefs[1])}

ПРОЦЕСС (обязательно):
1. Перепиши статью в чистый Markdown (~13000-16000 символов, на русском, без преамбулы "я изменил").
2. Запиши переписанный markdown в файл /tmp/day_${day}_new.md инструментом Write.
3. Обнови JSON, сохранив поля day/title/green без изменений, через Bash:
   python3 -c "import json;p='data/articles/day_${day}.json';d=json.load(open(p));d['body_markdown']=open('/tmp/day_${day}_new.md').read().strip();json.dump(d,open(p,'w'),ensure_ascii=False,indent=2)"
4. Верни ОДНОЙ строкой: 'day ${day} done, N chars'.

Финал статьи — плавный переход к действию/следующей теме. Можно блок "## Запомни" с опорными терминами дня.`,
    { label: `editor:d${day}`, phase: 'Rewrite' }
  )
)

return { done: results.filter(Boolean).length, of: DAYS.length, sample: results.slice(0, 3) }
